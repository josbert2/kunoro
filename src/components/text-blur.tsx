const TextAnimateBase = ({
  children,
  delay = 0,
  duration = 0.3,
  variants,
  className,
  segmentClassName,
  as: Component = "p",
  startOnView = true,
  once = false,
  by = "word",
  animation = "fadeIn",
  loopWords,
  rotateInterval = 2000,
  colors,
  ...props
}: TextAnimateProps) => {
  const MotionComponent = motion(Component);

  const [currentIndex, setCurrentIndex] = useState(0);
  const color = colors?.[currentIndex % (colors.length || 1)];

  useEffect(() => {
    if (!loopWords || loopWords.length === 0) return;
    const interval = setInterval(() => {
      setCurrentIndex((prev) => (prev + 1) % loopWords.length);
    }, rotateInterval);
    return () => clearInterval(interval);
  }, [loopWords, rotateInterval]);

  const textToAnimate =
    loopWords && loopWords.length > 0
      ? loopWords[currentIndex]
      : typeof children === "string"
        ? children
        : Array.isArray(children)
          ? children.join(" ")
          : "";

  let segments: string[] = [];
  switch (by) {
    case "word":
      segments = textToAnimate.split(/(\s+)/);
      break;
    case "character":
      segments = textToAnimate.split("");
      break;
    case "line":
      segments = textToAnimate.split("\n");
      break;
    case "text":
    default:
      segments = [textToAnimate];
      break;
  }

  const finalVariants = variants
    ? {
        container: {
          hidden: { opacity: 0 },
          show: {
            opacity: 1,
            transition: {
              opacity: { duration: 0.01, delay },
              delayChildren: delay,
              staggerChildren: duration / segments.length,
            },
          },
          exit: {
            opacity: 0,
            transition: {
              staggerChildren: duration / segments.length,
              staggerDirection: -1,
            },
          },
        },
        item: variants,
      }
    : animation
      ? {
          container: {
            ...defaultItemAnimationVariants[animation].container,
            show: {
              ...defaultItemAnimationVariants[animation].container.show,
              transition: {
                delayChildren: delay,
                staggerChildren: duration / segments.length,
              },
            },
            exit: {
              ...defaultItemAnimationVariants[animation].container.exit,
              transition: {
                staggerChildren: duration / segments.length,
                staggerDirection: -1,
              },
            },
          },
          item: defaultItemAnimationVariants[animation].item,
        }
      : {
          container: defaultContainerVariants,
          item: defaultItemVariants,
        };

  return (
    <AnimatePresence mode="popLayout">
      <MotionComponent
        key={`text-animate-${textToAnimate}`}
        variants={finalVariants.container as Variants}
        initial="hidden"
        whileInView={startOnView ? "show" : undefined}
        animate={startOnView ? undefined : "show"}
        exit="exit"
        className={cn("whitespace-pre-wrap", className)}
        viewport={{ once }}
        {...props}
      >
        {segments.map((segment, i) => (
          <motion.span
            key={`${by}-${segment}-${i}`}
            variants={finalVariants.item}
            custom={i * staggerTimings[by]}
            style={color ? { color } : undefined}
            className={cn(
              by === "line" ? "block" : "inline-block whitespace-pre",
              segmentClassName,
            )}
          >
            {segment}
          </motion.span>
        ))}
      </MotionComponent>
    </AnimatePresence>
  );
};

export const TextAnimate = memo(TextAnimateBase);
